# Task 5: Count Occurrences of a Substring in a String
# Sample Input:
#     String: "I love programming in Python, Python is great."
#     Substring: "Python"
# Expected Output:
#     Count of "Python": 2

input_string = "I love programming in Python, Python is great."
s= "Python"
count = input_string.count(s)
print("Count of python:", count)