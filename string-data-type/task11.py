# Task 11: Join a List of Strings into a Single String
# Sample Input:
#     List of Strings: ['Python', 'is', 'great']
#     Separator: " "
# Expected Output:
#     Joined String: "Python is great"

list_of_strings = ['Python', 'is', 'great']
separator = " "
a = separator.join(list_of_strings)
print("Joined String:",a)