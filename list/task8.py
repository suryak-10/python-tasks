# Task 8: Element Remover
# Sample Input:
#     List: ["red", "blue", "green", "yellow", "purple"]
#     User Input: "green"
# Expected Output:
#     List after removal: ["red", "blue", "yellow", "purple"]

a=["red", "blue", "green", "yellow", "purple"]
a.remove("green")
print("List after removal:",a)